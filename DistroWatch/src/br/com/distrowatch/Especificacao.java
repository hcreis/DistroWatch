package br.com.distrowatch;

public class Especificacao {

	private String tipoSo;
	private String baseadoEm;
	private String origem;
	private String arquitetura;

	public String getTipoSo() {
		return tipoSo;
	}

	public void setTipoSo(String tipoSo) {
		this.tipoSo = tipoSo;
	}

	public String getBaseadoEm() {
		return baseadoEm;
	}

	public void setBaseadoEm(String baseadoEm) {
		this.baseadoEm = baseadoEm;
	}

	public String getOrigem() {
		return origem;
	}

	public void setOrigem(String origem) {
		this.origem = origem;
	}

	public String getArquitetura() {
		return arquitetura;
	}

	public void setArquitetura(String arquitetura) {
		this.arquitetura = arquitetura;
	}

}
